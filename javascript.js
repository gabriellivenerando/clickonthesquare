const quadrado1 = document.querySelector('.div1')

quadrado1.style.backgroundColor = 'white';

function trocaDeCor1(){
    if(quadrado1.style.backgroundColor == 'white'){
        quadrado1.style.backgroundColor = 'darksalmon';
    }else{
        quadrado1.style.backgroundColor = 'white';
    }

}


